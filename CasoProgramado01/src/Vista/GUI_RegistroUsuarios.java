    public void cargarTipo()//COMBOBOX
    {
        this.jcb_Tipo.removeAllItems();
        
        for(int contador=0;contador<3;contador++){
            this.jcb_Tipo.addItem(""+contador);
        }
        this.jcb_Tipo.setSelectedIndex(1);
    }
    
     public String[] devolverInformacion()
    {
        String arreglo[];
        arreglo=new String[4];
        arreglo[0]=this.jt_NombreCompleto.getText();
        arreglo[1]=this.jt_Usuario.getText();
        arreglo[2]=this.jpf_Contrasena.getText();
        arreglo[3]=""+this.jcb_Tipo.getSelectedIndex();
         
        return arreglo;
    }
     public String devolverUsuario()
    {
        return jt_Usuario.getText();
    }
    
    public void mostrarInformacion(String arreglo[])
    {
        this.jt_NombreCompleto.setText(arreglo[0]);
        this.jpf_Contrasena.setText(arreglo[1]);
        this.jcb_Tipo.setSelectedIndex(Integer.parseInt(arreglo[2]));
    }
     public void limpiarInterfaz()
    {
        this.jt_NombreCompleto.setText("");
        this.jt_Usuario.setText("");
        this.jpf_Contrasena.setText("");
    }
     public void deshabilitarUsuario()
    {
        this.jt_NombreCompleto.setEnabled(true);
        this.jt_Usuario.setEnabled(false);
        this.jpf_Contrasena.setEnabled(true);
    }
    
    public void estadoInicial()
    {
       this.jt_NombreCompleto.setEnabled(false);
        this.jt_Usuario.setEnabled(true);
        this.jpf_Contrasena.setEnabled(false);
    }
    
    public void habilitarAgregar()
    {
        this.jt_NombreCompleto.setEnabled(true);
        this.jt_Usuario.setEnabled(true);
        this.jpf_Contrasena.setEnabled(true);
    }


